package com.soochna.dao;

/**
 * @author root
 *
 */
public class UserDetails {

	private String ministryHead;
	private String ministryName;
	private String uid;
	private String email;

	/**
	 * @return the ministryHead
	 */
	public String getMinistryHead() {
		return ministryHead;
	}

	/**
	 * @param ministryHead
	 *            the ministryHead to set
	 */
	public void setMinistryHead(String ministryHead) {
		this.ministryHead = ministryHead;
	}

	/**
	 * @return the ministryName
	 */
	public String getMinistryName() {
		return ministryName;
	}

	/**
	 * @param ministryName
	 *            the ministryName to set
	 */
	public void setMinistryName(String ministryName) {
		this.ministryName = ministryName;
	}

	/**
	 * @return the uid
	 */
	public String getUid() {
		return uid;
	}

	/**
	 * @param uid
	 *            the uid to set
	 */
	public void setUid(String uid) {
		this.uid = uid;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email
	 *            the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

}
